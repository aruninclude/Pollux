package cloud.web;

import org.springframework.stereotype.Controller;
import org.springframework.ui.Model;
import org.springframework.web.bind.annotation.PathVariable;
import org.springframework.web.bind.annotation.RequestMapping;
import org.springframework.web.bind.annotation.RequestMethod;
import org.springframework.web.bind.annotation.ResponseBody;

/**
 * 
 * @author Miguel Rojas (email.miguel.rojas@googlemail.com)
 *
 */
@Controller
public class CDMIServicesController 
{
	@RequestMapping( value="/properties/{containerName}/{imageId}", method=RequestMethod.GET )
	public String properties( @PathVariable String containerName, @PathVariable String imageId, Model model )
	{
		model.addAttribute( "key", "value" );  
		return "displayPet"; 
	}
	
	@RequestMapping ( value="/greeting/{name}", method=RequestMethod.GET )
	@ResponseBody
	public String greeting( @PathVariable String name )
	{
		System.out.println( this );
		return String.format( "Welcome %s", name );
	}
}
